package IfStatement;

public class IfElse {
    public static void main(String[] args) {
        int num1=20;
        int num2=30;
        if (num1<num2) {
            System.out.println("num1 is less then num2");
        }
        else{
            System.out.println("num1 is not less then num2");
        }
    }
}
